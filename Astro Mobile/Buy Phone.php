<script>
    $(document).ready(function () {
        $('.sb-device-carousel').on('afterChange', function () {
            changeSliderLabel();
        });
        $('.sb-device-carousel').on('init', function () {
            changeSliderLabel();
        });
    });
    function changeSliderLabel() {
        var lang = $('html').attr('lang').substring(0, 2).toLowerCase(),
            productName = $('.dd-device-title').text(),
            imageNumber = (lang === 'fr') ? "Image numéro " : "Image ",
            imageNumberOf = (lang === 'fr') ? " de " : " of ",
            viewImageNumber = (lang === 'fr') ? "Voir l'image numéro " : "View image ";
        var parent = document.querySelector('.sb-device-carousel');
        var slickdots = parent.querySelector('.slick-dots');
        var slicklist = parent.querySelector('.slick-list');
        var slickslide = slicklist.querySelectorAll('.slick-slide');
        var slickdotsli = slickdots.querySelectorAll('li');
        var slickbuttons = slickdots.querySelectorAll('button');
        var li = $('.sb-device-carousel').find('li');
        const $this = $('.sb-device-carousel').find('.slick-dots');
        const buttons = $this.find('button');
        li.on('keydown', function (e) {
            if (e.keyCode == 37 || e.keyCode == 39 || e.keyCode == 38 || e.keyCode == 40) {
                e.stopPropagation();
            }
        });
        setTimeout(function () {
            slickdotsli.forEach(function (elem) {
                elem.removeAttribute('role');
            });
            var slickHasAtt = slickdots.hasAttribute('role');
            if (slickHasAtt == true) {
                slickdots.removeAttribute('role');
            }
            slickslide.forEach(function (elem) {
                elem.removeAttribute('role');
            });
            slickbuttons.forEach(function (elem) {
                var selected = elem.getAttribute('aria-selected');
                elem.setAttribute('type', 'button');
                elem.setAttribute('tabIndex', '0');
                elem.removeAttribute('role');
                elem.removeAttribute('aria-selected');
                if (selected == "true") {
                    elem.setAttribute('aria-current', selected);

                } else {
                    elem.setAttribute('aria-current', false);
                }
            });
            buttons.each(function (slideCount) {
                $(this).attr('aria-label', viewImageNumber + (slideCount + 1) + imageNumberOf + productName);
            });
        }, 1);
    }
</script>



<script>
const imgs = document.querySelectorAll('.img-select a');
const imgBtns = [...imgs];
let imgId = 1;

imgBtns.forEach((imgItem) => {
    imgItem.addEventListener('click', (event) => {
        event.preventDefault();
        imgId = imgItem.dataset.id;
        slideImage();
    });
});

function slideImage(){
    const displayWidth = document.querySelector('.img-showcase img:first-child').clientWidth;

    document.querySelector('.img-showcase').style.transform = `translateX(${- (imgId - 1) * displayWidth}px)`;
}

window.addEventListener('resize', slideImage);
 </script>   
    
